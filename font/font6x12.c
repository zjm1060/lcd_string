static uint8_t *hzdot = NULL;

int Font_6x12_GetSymbol(tFont *font,unsigned short ch,tSymbol *symbol)
{
	int offset;

	if(ch >= 0x20 && ch < 127){
		offset = ch - 0x20;
		offset *= 12;
		symbol->h = 12;
		symbol->w = 6;
		symbol->symbol = &font->symbol_ascii[offset];
	}
	else{
		return -1;
		offset = 0;
		symbol->h = 12;
		symbol->w = 12;
#if 0
		while(hzIndex[offset] != 0){
			if(hzIndex[offset] == ch){
				//printf("%d ",GB);
				//DrawCH(adjx,adjy,&hzdot[i*24],SYMBOL_WIDTH,SYMBOL_HIGH);
				break;
			}
			offset++;
		}

		symbol->symbol = &hzdot[offset*24];
#endif

		if(hzdot == NULL){
			int fd=open("/data/hzdot.bin", O_RDONLY);

			int size = lseek(fd,0,SEEK_END);

			hzdot = mmap(NULL, size , PROT_READ, MAP_SHARED , fd , 0);
		}

		int hig=(ch&0xff00)>>8;
		int low=ch&0x00ff;

		hig-=0xa1;
		low-=0xa1;
		int HZ_addr=(hig*94)+low;
		HZ_addr *= 24;

		symbol->symbol = &hzdot[HZ_addr];
	}

	return 0;
}

int Font_6x12_GetSymbolWidth(tFont *font,unsigned short ch)
{
	if(ch < 127)
		return 6;
	else
		return 12;
}

int Font_6x12_GetSymbolHight(tFont *font,unsigned short ch)
{
	return 12;
}


